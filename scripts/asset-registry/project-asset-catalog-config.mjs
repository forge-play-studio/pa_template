import fs from 'node:fs/promises';
import path from 'node:path';
import { toImportName } from './core.mjs';

const cwd = process.cwd();
const assetsDir = path.resolve(cwd, 'src/assets');
const importedDir = path.resolve(cwd, 'src/assets/imported');
const generatedDir = path.resolve(cwd, 'src/assets/generated');

export const projectAssetCatalogConfig = {
  cwd,
  assetsDir,
  importedDir,
  generatedDir,
  manifestPath: path.join(generatedDir, 'asset-catalog.manifest.json'),
  registryPath: path.join(generatedDir, 'asset-catalog.generated.ts'),
  scenePath: path.resolve(cwd, 'src/config/scene.json'),
  rulesPath: path.resolve(cwd, 'src/config/scene-json-v2-rules.json'),
  supportedExtensions: ['.glb', '.gltf', '.png', '.jpg', '.jpeg', '.webp', '.mp3', '.wav', '.ogg', '.m4a'],
  commands: {
    register: 'npm run asset:register',
    unregister: 'npm run asset:unregister',
  },
  async loadRules() {
    return JSON.parse(await fs.readFile(this.rulesPath, 'utf8'));
  },
  async resolveAssetMetadata({ kind, sourcePath, existingMetadata, payloadMetadata, guid, assetId }) {
    const metadata = {
      ...(existingMetadata ?? {}),
      ...(payloadMetadata ?? {}),
    };
    if (kind !== 'model') return Object.keys(metadata).length > 0 ? metadata : null;

    const materialSlots = await extractModelMaterialSlots(sourcePath, {
      assetGuid: guid,
      assetId,
      existingMaterialSlots: metadata.materialSlots,
    });
    if (materialSlots.length === 0) {
      delete metadata.materialSlots;
    } else {
      metadata.materialSlots = materialSlots;
    }
    return Object.keys(metadata).length > 0 ? metadata : null;
  },
  relativeImportedPath(kind, fileName) {
    if (kind === 'model') return `../imported/${fileName}`;
    if (kind === 'sound') return `../imported/sounds/${fileName}`;
    return `../imported/textures/${fileName}`;
  },
  publicUrlForImportedAsset(kind, fileName) {
    if (kind === 'model') return `/src/assets/imported/${fileName}`;
    if (kind === 'sound') return `/src/assets/imported/sounds/${fileName}`;
    return `/src/assets/imported/textures/${fileName}`;
  },
  publicUrlForCatalogRelativePath(relativePath) {
    const targetPath = path.resolve(generatedDir, relativePath);
    const relativeToCwd = path.relative(cwd, targetPath).split(path.sep).join('/');
    return `/${relativeToCwd}`;
  },
  toImportName,
  generateRegistry(manifest) {
    const imports = manifest
      .map((entry) => `import ${toImportName(entry.assetId)} from '${entry.relativePath}?url';`)
      .join('\n');
    const catalogEntries = manifest
      .map((entry) => {
        const pieces = [
          `guid: ${JSON.stringify(entry.guid)}`,
          `assetId: ${JSON.stringify(entry.assetId)}`,
          `kind: ${JSON.stringify(entry.kind)}`,
          `displayName: ${JSON.stringify(entry.displayName)}`,
          `url: ${toImportName(entry.assetId)}`,
          `relativePath: ${JSON.stringify(entry.relativePath)}`,
          entry.originalFileName ? `originalFileName: ${JSON.stringify(entry.originalFileName)}` : '',
          entry.codeKey ? `codeKey: ${JSON.stringify(entry.codeKey)}` : '',
          typeof entry.placeable === 'boolean' ? `placeable: ${JSON.stringify(entry.placeable)}` : '',
          entry.contentHash ? `contentHash: ${JSON.stringify(entry.contentHash)}` : '',
          typeof entry.byteSize === 'number' ? `byteSize: ${JSON.stringify(entry.byteSize)}` : '',
          entry.external ? `external: ${JSON.stringify(entry.external)}` : '',
          entry.metadata ? `metadata: ${JSON.stringify(entry.metadata)}` : '',
          entry.createdAt ? `createdAt: ${JSON.stringify(entry.createdAt)}` : '',
          entry.updatedAt ? `updatedAt: ${JSON.stringify(entry.updatedAt)}` : '',
        ].filter(Boolean).join(', ');
        return `  ${JSON.stringify(entry.assetId)}: { ${pieces} },`;
      })
      .join('\n');
    const idEntries = manifest
      .filter((entry) => entry.codeKey)
      .map((entry) => `  ${JSON.stringify(entry.codeKey)}: ${JSON.stringify(entry.assetId)},`)
      .join('\n');

    return [
      '// This file is generated by scripts/register-asset.mjs.',
      '// Do not edit by hand.',
      '',
      imports,
      imports ? '' : '',
      'export type GeneratedAssetCatalogKind = "model" | "texture" | "image" | "sound";',
      '',
      'export interface GeneratedAssetCatalogEntry {',
      '  guid: string;',
      '  assetId: string;',
      '  kind: GeneratedAssetCatalogKind;',
      '  displayName: string;',
      '  url: string;',
      '  relativePath: string;',
      '  originalFileName?: string;',
      '  codeKey?: string;',
      '  placeable?: boolean;',
      '  contentHash?: string;',
      '  byteSize?: number;',
      '  external?: { platformAssetId?: string; assetPath?: string; assetUrl?: string };',
      '  metadata?: Record<string, unknown>;',
      '  createdAt?: string;',
      '  updatedAt?: string;',
      '}',
      '',
      'export const GENERATED_ASSET_CATALOG: Record<string, GeneratedAssetCatalogEntry> = {',
      catalogEntries,
      '};',
      '',
      'export const GENERATED_ASSET_URL_MAP = Object.fromEntries(',
      '  Object.entries(GENERATED_ASSET_CATALOG).map(([assetId, entry]) => [assetId, entry.url]),',
      ') as Record<string, string>;',
      '',
      'export const GENERATED_MODEL_ASSET_URL_MAP = Object.fromEntries(',
      '  Object.entries(GENERATED_ASSET_CATALOG).filter(([, entry]) => entry.kind === "model").map(([assetId, entry]) => [assetId, entry.url]),',
      ') as Record<string, string>;',
      '',
      'export const GENERATED_TEXTURE_ASSET_URL_MAP = Object.fromEntries(',
      '  Object.entries(GENERATED_ASSET_CATALOG).filter(([, entry]) => entry.kind === "texture").map(([assetId, entry]) => [assetId, entry.url]),',
      ') as Record<string, string>;',
      '',
      'export const GENERATED_IMAGE_ASSET_URL_MAP = Object.fromEntries(',
      '  Object.entries(GENERATED_ASSET_CATALOG).filter(([, entry]) => entry.kind === "image").map(([assetId, entry]) => [assetId, entry.url]),',
      ') as Record<string, string>;',
      '',
      'export const GENERATED_SOUND_ASSET_URL_MAP = Object.fromEntries(',
      '  Object.entries(GENERATED_ASSET_CATALOG).filter(([, entry]) => entry.kind === "sound").map(([assetId, entry]) => [assetId, entry.url]),',
      ') as Record<string, string>;',
      '',
      'export const ASSET_IDS = {',
      idEntries,
      '} as const satisfies Record<string, string>;',
      '',
    ].join('\n');
  },
};

