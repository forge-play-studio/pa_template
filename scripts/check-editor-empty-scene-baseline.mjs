#!/usr/bin/env node

import assert from 'node:assert/strict';
import fs from 'node:fs/promises';
import path from 'node:path';

const projectRoot = process.cwd();
const editorScene = await readJson('src/config/editor-scene.json');
const sceneConfig = await readJson('src/config/scene.json');

const EDITOR_SYSTEM_OBJECT_IDS = ['root', 'main_camera', 'environment_light', 'sun_light'];
const RUNTIME_SYSTEM_NODE_IDS = ['main_camera', 'environment_light', 'sun_light'];

assert.equal(editorScene.schemaVersion, 1, 'editor-scene.json must remain authoring schema v1');
assert.deepEqual(editorScene.assets ?? [], [], 'empty editor baseline must not register scene assets');
assert.deepEqual(editorScene.scene?.materialAssets ?? [], [], 'empty editor baseline must not include authored material assets');

const editorObjects = editorScene.scene?.gameObjects ?? [];
assert.deepEqual(editorObjects.map(object => object.id), EDITOR_SYSTEM_OBJECT_IDS, 'empty editor baseline must only contain system objects');

const objectById = new Map(editorObjects.map(object => [object.id, object]));
assertSystemTransform(objectById.get('root'), { id: 'root', name: 'Root', parentId: undefined });
assertSystemTransform(objectById.get('main_camera'), { id: 'main_camera', name: 'Main Camera', parentId: 'root', transformType: 'camera' });
assertSystemTransform(objectById.get('environment_light'), { id: 'environment_light', name: 'Environment Light', parentId: 'root', transformType: 'light' });
assertSystemTransform(objectById.get('sun_light'), { id: 'sun_light', name: 'Directional Light', parentId: 'root', transformType: 'light' });

for (const object of editorObjects) {
  assert.notEqual(object.kind, 'instance', `${object.id} must not be an asset instance`);
  assert.notEqual(object.kind, 'primitive', `${object.id} must not be a sample primitive`);
  assert.notEqual(object.kind, 'group', `${object.id} must not be a sample group`);
  assert.notEqual(object.transformType, 'groundDecal', `${object.id} must not be a live fixture ground decal`);
}

assert.equal(sceneConfig.schemaVersion, 3, 'scene.json must use runtime schema v3');
assert.equal(sceneConfig.scene?.rootId, 'root', 'runtime scene rootId');
assert.deepEqual(sceneConfig.scene?.assets ?? [], [], 'empty runtime baseline must not include scene assets');
assert.deepEqual(sceneConfig.scene?.materialAssets ?? [], [], 'empty runtime baseline must not include material assets');
assert.deepEqual(sceneConfig.scene?.materials ?? [], [], 'empty runtime baseline must not include compiled materials');
assert.deepEqual(sceneConfig.scene?.textures ?? [], [], 'empty runtime baseline must not include compiled textures');

const runtimeNodes = sceneConfig.scene?.nodes ?? [];
assert.deepEqual(runtimeNodes.map(node => node.id), RUNTIME_SYSTEM_NODE_IDS, 'empty runtime baseline must only contain system nodes');
for (const node of runtimeNodes) {
  assert.notEqual(node.kind, 'instance', `${node.id} must not be a runtime asset instance`);
  assert.notEqual(node.kind, 'primitive', `${node.id} must not be a runtime sample primitive`);
  assert.equal(node.parentId, 'root', `${node.id} must stay parented to root`);
}

assert.equal(
  sceneConfig.meta?.generatedFrom?.revision,
  editorScene.meta?.authoringSource?.revision,
  'runtime scene must be compiled from the checked-in editor scene revision',
);
assert.equal(
  String(sceneConfig.meta?.generatedFrom?.compilerVersion),
  '2',
  'runtime scene must be generated by compiler v2',
);

console.log('[check-editor-empty-scene-baseline] empty scene baseline verified');

function assertSystemTransform(object, expected) {
  assert(object, `${expected.id} must exist`);
  assert.equal(object.id, expected.id, `${expected.id}.id`);
  assert.equal(object.name, expected.name, `${expected.id}.name`);
  assert.equal(object.active, true, `${expected.id}.active`);
  if (expected.parentId === undefined) {
    assert.equal(Object.hasOwn(object, 'parentId'), false, `${expected.id}.parentId`);
  } else {
    assert.equal(object.parentId, expected.parentId, `${expected.id}.parentId`);
  }
  if (expected.transformType) assert.equal(object.transformType, expected.transformType, `${expected.id}.transformType`);
  assert(object.components?.some(component => component?.type === 'Transform'), `${expected.id} must have a Transform component`);
}

async function readJson(relativePath) {
  return JSON.parse(await fs.readFile(path.join(projectRoot, relativePath), 'utf8'));
}
