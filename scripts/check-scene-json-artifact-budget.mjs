#!/usr/bin/env node

import assert from 'node:assert/strict';
import fs from 'node:fs/promises';
import path from 'node:path';

const root = process.env.SCENE_JSON_ARTIFACT_ROOT
  ? path.resolve(process.env.SCENE_JSON_ARTIFACT_ROOT)
  : process.cwd();
const artifacts = [
  {
    name: 'editor-scene.json',
    path: 'src/config/editor-scene.json',
    baselineBytes: 287_264,
    maximumBytes: 115 * 1024,
  },
  {
    name: 'scene.json',
    path: 'src/config/scene.json',
    baselineBytes: 314_078,
    maximumBytes: 126 * 1024,
  },
];

const loaded = new Map();
for (const artifact of artifacts) {
  const text = await fs.readFile(path.join(root, artifact.path), 'utf8');
  const value = JSON.parse(text);
  const bytes = Buffer.byteLength(text);
  assert.ok(bytes <= artifact.maximumBytes, `${artifact.name} is ${bytes} bytes; maximum is ${artifact.maximumBytes}`);
  assert.ok(
    bytes <= Math.floor(artifact.baselineBytes * 0.4),
    `${artifact.name} must remain at least 60% below its ${artifact.baselineBytes}-byte baseline; got ${bytes}`,
  );
  assert.equal(text, `${JSON.stringify(value, null, 2)}\n`, `${artifact.name} must use canonical two-space JSON with a trailing newline`);
  assertCanonicalNumbers(value, `$${artifact.name}`);
  loaded.set(artifact.name, { bytes, value });
}

const editorScene = loaded.get('editor-scene.json').value;
assert.equal(editorScene.schemaVersion, 1, 'editor-scene.json must remain authoring schema v1');
for (const [index, asset] of (editorScene.assets ?? []).entries()) {
  if (asset?.type !== 'glb') continue;
  const unexpected = Object.keys(asset).filter(key => !['id', 'guid', 'type'].includes(key));
  assert.deepEqual(unexpected, [], `editor-scene.json asset ${index} must be a lean catalog reference`);
}

const runtimeScene = loaded.get('scene.json').value;
assert.equal(runtimeScene.schemaVersion, 3, 'scene.json must use runtime schema v3');
assert.equal(String(runtimeScene.meta?.generatedFrom?.compilerVersion), '2', 'scene.json must be generated by compiler v2');
for (const [index, asset] of (runtimeScene.scene?.assets ?? []).entries()) {
  for (const field of ['metadata', 'assetAnalysis', 'displayName', 'category']) {
    assertNoField(asset, field, `$.scene.assets[${index}]`);
  }
}

console.log('[scene-json-artifact-budget]', Object.fromEntries(
  [...loaded].map(([name, entry]) => [name, entry.bytes]),
));

function assertCanonicalNumbers(value, valuePath) {
  if (typeof value === 'number') {
    assert.ok(Number.isFinite(value), `${valuePath} must be finite`);
    assert.ok(!Object.is(value, -0), `${valuePath} must normalize -0 to 0`);
    assert.ok(
      Number.isInteger(value) || value === Number(value.toFixed(4)),
      `${valuePath} must use at most four decimal places; got ${value}`,
    );
    return;
  }
  if (Array.isArray(value)) {
    value.forEach((entry, index) => assertCanonicalNumbers(entry, `${valuePath}[${index}]`));
    return;
  }
  if (!value || typeof value !== 'object') return;
  for (const [key, entry] of Object.entries(value)) {
    assertCanonicalNumbers(entry, `${valuePath}.${key}`);
  }
}

function assertNoField(value, field, valuePath) {
  if (Array.isArray(value)) {
    value.forEach((entry, index) => assertNoField(entry, field, `${valuePath}[${index}]`));
    return;
  }
  if (!value || typeof value !== 'object') return;
  assert.ok(!(field in value), `${valuePath} must not contain ${field}`);
  for (const [key, entry] of Object.entries(value)) {
    assertNoField(entry, field, `${valuePath}.${key}`);
  }
}
